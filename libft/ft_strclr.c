#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
		i++;
	while (j < i)
	{
		s[j] = '\0';
		j++;
	}
}
