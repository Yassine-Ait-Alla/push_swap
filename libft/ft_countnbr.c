#include "libft.h"

int		ft_countnbr(int nbr)
{
	int		i;

	i = 0;
	if (nbr < 10 && nbr >= 0)
		return (1);
	while (nbr)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
